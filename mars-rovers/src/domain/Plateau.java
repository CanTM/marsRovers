package domain;

public class Plateau {
	private int width;
	private int hight;
	

	public Plateau(int width, int hight) {
		this.width = width;
		this.hight = hight;
	}

	public int getHight() {
		return hight;
	}

	public void setHight(int hight) {
		this.hight = hight;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

}
